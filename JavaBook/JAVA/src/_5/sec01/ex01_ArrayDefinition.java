package _5.sec01;

public class ex01_ArrayDefinition {
    public static void main(String[] args) {
        //배열의 선언 방법 1
        int[] array1 = new int[3];
        int[] array2;
        array2 = new int[3];

        //배열의 선언 방법 2
        int array3[] = new int[3];
        int array4[];
        array4 = new int[3];

        //다양한 배여 선언 (기본 자료형 배열, 참조 자료형 배열)
        boolean[] array5 = new boolean[4];
        int[] array6 = new int[4];
        double[] array7 = new double[3];
        String[] array8 = new String[9];

    }
}
